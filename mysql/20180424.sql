#测试何种指标:
吞吐量:吞吐量是指的是单位时间内的事务处理数。常用的测试单位是TPS每秒事务数，有时也采用每分钟事务数TPM
响应时间或者延迟：这个指标用于测试任务所需的整体时间
并发性:并发性是一个非常重要又经常被误解和误用的指标。 例如，它经常被表示多少用户在同一时间浏览一个web站点，经常使用的指标是有多少个会话。然而，HTTP协议是无状态的，大多数用户只是简单读取浏览器上显示的信息，这并不等同于web服务器的并发性。而且web服务器并发性并不等同于数据库的并发性，而仅仅表示会话存储机制可以处理多少数据的能力。
web服务器的并发性更准确的度量指标，应该是在任意时间有多少同时发生的并发请求

web服务器的高并发，一般会导致数据库的高并发，但服务器采用的语言和工具集也都会由此影响。
注意不要将创建数据库连接和并发性搞混淆
